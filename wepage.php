        <section id="about">
            <h2>About Us</h2>
            <p><strong>Government ITI Kulpi running under PTP </strong>has been set up by Government of West Bengal under the control of Directorate of Industrial Training (DIT), Technical Education, Training & Skill Development Department, Govt. of West Bengal, Karigori Bhawan, Kolkata-700160. Government ITI Kulpi running under PTP is operated by Keertika Education & Associates LLP as PPP mode. The ITI was accredited by Quality Council of India (QCI) for compliance with the Norms & Standards as laid down by the National Council for Vocational Training (NCVT), an advisory body, set up by the Government of India, under Ministry of Skill Development and Entrepreneurship, Govt. of India. Based on the accreditation by QCI, Government ITI Kulpi running under PTP was subsequently affiliated by NCVT to operate eight trades under Craftsman Training Scheme viz. Fitter, Electrician, Surveyor, Draughtsman (Civil), Welder, Information Technology, Mechanic(Refrigeration and Air-Conditioning). On completion of trade courses and passing the All India Trade Test (AITT) successfully the trainees are awarded National Trade Certificate (NTC) by NCVT.
</p>
